function normalizeBlockedPrefixWord(value: string) {
  return value.trim().toLowerCase().replace(/[._-]/g, "")
}

const blockedTempEmailLocalPartSet = new Set(
  BLOCKED_TEMP_EMAIL_LOCAL_PARTS.map(normalizeBlockedPrefixWord)
)

export function isBlockedTempEmailPrefix(localPart: string) {
  const normalized = normalizeBlockedPrefixWord(localPart)
  if (!normalized) {
    return false
  }

  if (blockedTempEmailLocalPartSet.has(normalized)) {
    return true
  }

  const withoutTrailingDigits = normalized.replace(/\d+$/, "")
  return withoutTrailingDigits !== normalized && blockedTempEmailLocalPartSet.has(withoutTrailingDigits)
}
