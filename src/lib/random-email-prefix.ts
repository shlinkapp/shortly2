function pickRandomValue(values: readonly string[], random: () => number) {
  return values[Math.floor(random() * values.length)] || values[0]
}

export function generateRandomEmailPrefix(random: () => number = Math.random) {
  const firstName = pickRandomValue(COMMON_EMAIL_FIRST_NAMES, random)
  const lastName = pickRandomValue(COMMON_EMAIL_LAST_NAMES, random)
  const digits = Math.floor(random() * 1000).toString().padStart(3, "0")

  return `${firstName}-${lastName}${digits}`
}
